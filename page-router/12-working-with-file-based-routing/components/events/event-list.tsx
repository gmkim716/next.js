import EventItem from "./event-item";

import classes from "../../styles/event-list.module.css";

export default function EventList(props: { items: any[] }) {
  const { items } = props;

  return (
    <ul className={classes.list}>
      {items.map((event) => (
        <EventItem
          key={event.id}
          id={event.id}
          title={event.title}
          location={event.location}
          date={event.date}
          image={event.image}
        />
      ))}
    </ul>
  );
}
